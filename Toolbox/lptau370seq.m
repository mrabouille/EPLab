function [lpmat] = lptau370seq(N,k)

% [lpmat] = lptauSEQ(Nsam,Nvar)
%
% function call LPTAU and generates a sample of dimension N for a
% number of parameters k

clear lptau
lpmat = zeros(N, k);
for j=1:N,
    lpmat(j,:)=lptau(j,k);
end
return


